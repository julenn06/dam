package Ariketa_5;

public class eragiketa {
	/**
	 * 
	 * @param a aldagaia jasotzen du eta b aldagaiarekin aukeratu den aukeraren
	 *          arabera eragiketa bat egiten du
	 * @param b aldagaia jasotzen du eta a aldagaiarekin aukeratu den aukeraren
	 *          arabera eragiketa bat egiten du
	 * @return sortu den eragiketa itzultzen du.
	 */

	public static double batura(double a, double b) {

		return a + b;
	}

	public static double kendu(double a, double b) {

		return a - b;
	}

	public static double biderkatu(double a, double b) {

		return a * b;
	}

	public static double zatitu(double a, double b) {

		return a / b;
	}
}